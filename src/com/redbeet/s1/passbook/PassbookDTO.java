package com.redbeet.s1.passbook;

public class PassbookDTO {

	private String name;
	private long num;
	private double irate;
	private String sell;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public long getNum() {
		return num;
	}
	public void setNum(long num) {
		this.num = num;
	}
	public double getIrate() {
		return irate;
	}
	public void setIrate(double irate) {
		this.irate = irate;
	}
	public String getSell() {
		return sell;
	}
	public void setSell(String sell) {
		this.sell = sell;
	}
	
	
}
